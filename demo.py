def demo_research_workflow():
    """Demonstrate the research workflow."""
    print("=" * 60)
    print("ğŸ” CONTENT & RESEARCH AGENT DEMO")
    print("=" * 60)
    
    # Initialize the agent
    agent = ContentResearchAgent()
    
    # Example topics for research
    topics = [
        "sustainable energy technologies 2024",
        "quantum computing breakthroughs",
        "AI ethics in healthcare"
    ]
    
    for i, topic in enumerate(topics, 1):
        print(f"\nğŸ“š Example {i}: Researching '{topic}'")
        print("-" * 40)
        
        # Step 1: Research the topic
        print("ğŸ” Starting research...")
        research_result = agent.research_topic(topic)
        
        print(f"âœ… Research completed!")
        print(f"ğŸ“Š Sources queried: {len(research_result['research_results'])}")
        print(f"ğŸ“ Summary preview: {research_result['summary'][:150]}...")
        
        if research_result.get('notion_url'):
            print(f"ğŸ“‹ Stored in Notion: {research_result['notion_url']}")
        
        # Step 2: Create content draft
        print("\nâœï¸ Creating content draft...")
        draft = agent.create_content_draft(topic, "article")
        
        if draft:
            print(f"âœ… Content draft created!")
            print(f"ğŸ“ Title: {draft.title}")
            print(f"ğŸ“ Content length: {len(draft.content)} characters")
            print(f"ğŸ“š Based on {len(draft.source_material)} research sources")
            print(f"ğŸ¤– Generated by: {draft.author_agent}")
            print(f"ğŸ“„ Preview: {draft.content[:200]}...")
        else:
            print("âŒ Failed to create content draft")
            continue
        
        # Step 3: Generate speech (demo)
        print("\nğŸ¤ Generating speech preview...")
        speech_result = agent.generate_speech(draft.content[:300])  # First 300 chars
        print(f"ğŸµ Speech file: {speech_result['audio_path']}")
        print(f"â±ï¸ Estimated duration: {speech_result['duration']:.1f} seconds")
        
        # Step 4: Generate video (demo)
        print("\nğŸ¬ Generating video preview...")
        video_result = agent.generate_video(draft.content[:200])  # First 200 chars as script
        print(f"ğŸ“¹ Video file: {video_result['video_path']}")
        print(f"â±ï¸ Estimated duration: {video_result['duration']:.1f} seconds")
        
        print(f"\nâœ¨ Workflow completed for: {topic}")
        
        if i < len(topics):
            print("\n" + "=" * 40)


